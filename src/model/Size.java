package model;

public class Size {

	private int xCor;
	private int yCor;
	private int width;
	private int height;
	
	public int getXCor() {	
		return xCor;
	}
	public int getYCor() {
		return xCor;
	}
	public int getWidth() {
		return width;
	}
	public int getHeight() {
		return height;
	}
	
	public void setSize(int xCor, int yCor, int width, int height) {
		
		this.xCor = xCor;
		this.yCor = yCor;
		this.width = width;
		this.height = height;
	}
}
